package com.withstars.domain;

public class Tab {
    private Integer id;

    private Integer tabName;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTabName() {
        return tabName;
    }

    public void setTabName(Integer tabName) {
        this.tabName = tabName;
    }
}